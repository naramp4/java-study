package variable;

public class Variable2 {
	public static void main(String[] args) {
		// 변수 선언
		int a;
		// 10의 값을 20으로 변경해보자
		a = 10; // 10 -> 20

		System.out.println(a);
		System.out.println(a);
		System.out.println(a);

		/*
		* 숫자 정수를 보관할 수 있는 a라는 저장소를 만든다. 이것을 변수라 한다.
		* 이렇게 변수를 만드는 것을 변수 선언이라 한다. ex) int a;
		* 자바에서는 자바스크립트와 다르게 데이터 형태에 따라 지정해줘야 한다.
		* 방금 예시에서는 1,2,3,0,-1,-2,-3과 같은 정수를 넣을 수 있는 int형이다.
		* a = 10과 같이 처음으로 값을 대입해서 저장하는 것을 변수 초기화라 한다.
		* 참고) 여기서 =는 수학에서 말하는 두 값이 같다 라는 뜻이 아닌 = 오른쪽에 있는 값을 왼쪽에 저장한다는 뜻이다.
		* */
	}
}
